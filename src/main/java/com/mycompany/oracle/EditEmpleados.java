    public void Llenar_Combo() {
        ComboEmpleados.removeAllItems(); //elimina los datos que pudieran existir previamente en el combo
        Empleados obj = new Empleados();
        ArrayList lista =obj.Combo_Empleados();
        Iterator iter = lista.iterator();
        while (iter.hasNext()) {
            Empleados emp = (Empleados) iter.next();
            ComboEmpleados.addItem(emp);
        }
    }
    
