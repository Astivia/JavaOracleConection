    private void btn_BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BorrarActionPerformed
        Empleados em = (Empleados) ComboEmpleados.getSelectedItem();
        this.setId_Empleado(em.getId());
        int r = em.Elimina_Empleado(this.getId_Empleado());
        if (r!=0) {
            JOptionPane.showMessageDialog(null, "Se Elimino el Empleado");
            Llenar_Combo();
            ComboEmpleados.setSelectedItem(null);
            Name.setEnabled(false);
            LastName.setEnabled(false);
            LastName2.setEnabled(false);
            Salary.setEnabled(false);
            Age.setEnabled(false);
            Email.setEnabled(false);
            Phone.setEnabled(false);
            this.setId_Empleado(0);
            btn_Guardar.setEnabled(false);
            btn_Borrar.setEnabled(false);
        }

    }//GEN-LAST:event_btn_BorrarActionPerformed

