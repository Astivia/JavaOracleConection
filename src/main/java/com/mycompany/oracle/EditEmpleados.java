    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        if (Name.getText().equals("") || LastName.getText().equals("") || LastName2.getText().equals("")
             || Integer.parseInt(Age.getValue().toString()) <= 0 ||Email.getText().equals("")||Salary.getText().equals("")||Phone.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor de capturar los datos faltantes");
        } else {
            Empleados em = new Empleados();
            
            int r= em.Actualizar_Empleado( Name.getText(), LastName.getText(), LastName2.getText(), Integer.parseInt(Age.getValue().toString()), 
                    Email.getText(), Double.parseDouble(Phone.getText()),Double.parseDouble(Salary.getText()),this.getId_Empleado());
            if (r != 0) {
                Name.setText("");LastName.setText("");LastName2.setText("");Email.setText("");Salary.setText("");Age.setValue(0);Phone.setText("");
                ComboEmpleados.setSelectedItem(null);
                JOptionPane.showMessageDialog(null, "Datos Modificados correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Ocurrio un error");
            }
        }
    }//GEN-LAST:event_btn_GuardarActionPerformed

    private void btn_SeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SeleccionarActionPerformed
        Empleados empleado = (Empleados) ComboEmpleados.getSelectedItem();
        if (empleado ==null) {
            JOptionPane.showMessageDialog(null,"Seleccione un Empleado");
        }else{
            Name.setText(empleado.getNombre()); Name.setEnabled(true);
            LastName.setText(empleado.getAp_pat());LastName.setEnabled(true);
            LastName2.setText(empleado.getAp_mat());LastName2.setEnabled(true);
            Salary.setText(String.valueOf(empleado.getSalario()));Salary.setEnabled(true);
            Age.setValue(empleado.getEdad());Age.setEnabled(true);
            Email.setText(empleado.getEmail());Email.setEnabled(true);
            Phone.setText(String.valueOf(empleado.getNumero()));Phone.setEnabled(true);
            this.setId_Empleado(empleado.getId());
            btn_Guardar.setEnabled(true);btn_Borrar.setEnabled(true);
        }

    }//GEN-LAST:event_btn_SeleccionarActionPerformed

